def matrix_kramer(A, b):
    if A.shape[0] != A.shape[1]:
        raise Exception('ะะฐะทะผะตัะฝะพััั ะฝะต ัะพะฒะฟะฐะดะฐะตั')
    det = la.det(A)
    R = []
    for i in range(A.shape[0]):
        A_s = A.copy()
        A_s.T[i] = b
        R.append(la.det(A_s) / det)
    return R


